package cn.hzu.domain;

public class Floor {
	
	private String fid;
	private String flevel;
	private String froom;
	private String fexpect;
	private String freal;

	public Floor() {
	}

	public String getFid() {
		return fid;
	}

	public void setFid(String fid) {
		this.fid = fid;
	}

	public String getFlevel() {
		return flevel;
	}

	public void setFlevel(String flevel) {
		this.flevel = flevel;
	}

	public String getFroom() {
		return froom;
	}

	public void setFroom(String froom) {
		this.froom = froom;
	}

	public String getFexpect() {
		return fexpect;
	}

	public void setFexpect(String fexpect) {
		this.fexpect = fexpect;
	}

	public String getFreal() {
		return freal;
	}

	public void setFreal(String freal) {
		this.freal = freal;
	}

}
